function dashboardTemplate(spec: SkillSpec): string {
  const pc = pascal(spec.skill.name)
  return `/** ${spec.skill.name} dashboard — Generated by Feature Builder. */
import { useState, useEffect } from 'react'
import { api } from '@/lib/admin/api-client'

export function Feature${pc}Dashboard() {
  const [count, setCount] = useState(0)
  const [loading, setLoading] = useState(true)

  useEffect(() => {
    api.collections.list('${spec.skill.name}').then(res => {
      if (res.ok && res.data) setCount((res.data as any).total || 0)
      setLoading(false)
    })
  }, [])

  if (loading) return <div className="glass-panel" style={{ padding: '2rem', textAlign: 'center' }}>Loading…</div>

  return (
    <div>
      <h1 style={{ fontSize: '1.1rem', fontWeight: 700, color: '#1e293b', marginBottom: '1rem' }}>${spec.treeidIntegration.navItem.label}</h1>
      <div className="glass-card" style={{ padding: '1.5rem', borderRadius: '10px', textAlign: 'center' }}>
        <div style={{ fontSize: '2rem', fontWeight: 700, color: '#3b82f6' }}>{count}</div>
        <div style={{ fontSize: '0.8rem', color: '#64748b' }}>Total entries</div>
      </div>
    </div>
  )
}
`
}

export function generateAdminComponents(spec: SkillSpec): FileDescriptor[] {
  const base = `src/components/admin/${spec.skill.name}`
  const ui = spec.treeidIntegration.uiNeeds

  if (ui === 'none') return []
  if (ui === 'dashboard') {
    return [{ path: `${base}/feature-${spec.skill.name}-dashboard.tsx`, content: dashboardTemplate(spec) }]
  }
  if (ui === 'form') {
    return [{ path: `${base}/feature-${spec.skill.name}-form.tsx`, content: formTemplate(spec) }]
  }
  // list-detail (default)
  return [
    { path: `${base}/feature-${spec.skill.name}-list.tsx`, content: listTemplate(spec) },
    { path: `${base}/feature-${spec.skill.name}-editor.tsx`, content: editorTemplate(spec) },
  ]
}
